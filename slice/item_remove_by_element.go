package slice

func RemoveString(slice []string, el string) []string {
	var newSlice []string
	for _, _el := range slice {
		// If it's not the same then add to the new slice
		if _el != el {
			newSlice = append(newSlice, _el)
		}
	}
	return newSlice
}

